// +build ignore

package main

import (
	"bytes"
	"fmt"
	"go/format"
	"io/ioutil"
	"os"
	"path/filepath"
	"text/template"
	"time"

	"github.com/haleyrc/forms/fdl"
	"github.com/haleyrc/forms/library"
)

func main() {
	tmpl := template.Must(template.New("").Parse(formTemplate))

	iter := library.Forms.Iter()
	for form := iter.Next(); form != nil; form = iter.Next() {
		if err := form.Valid(); err != nil {
			panic(err)
		}

		fileName := fmt.Sprintf("form_%s.go", form.Code)
		path := filepath.Join("..", "form", fileName)

		buff := bytes.Buffer{}
		if err := tmpl.Execute(&buff, struct {
			Timestamp time.Time
			FileName  string
			Form      fdl.Form
		}{
			Timestamp: time.Now(),
			FileName:  fileName,
			Form:      *form,
		}); err != nil {
			panic(err)
		}

		formatted, err := format.Source(buff.Bytes())
		if err != nil {
			panic(err)
		}

		if err := ioutil.WriteFile(path, formatted, os.ModePerm); err != nil {
			panic(err)
		}
	}
}

var formTemplate = `// Code generated by go generate; DO NOT EDIT.
//
// This file was generated at:
// {{ .Timestamp }}
//
// Data loaded from library.Forms.
//
package form

import (
	"fmt"

	"github.com/haleyrc/forms/fdl"
	"github.com/haleyrc/forms/frl"
)

{{ range .Form.Nodes }}
{{- .Types }}
{{- end -}}

// Form{{ .Form.Code }}
//
// {{ .Form.Description }}
type Form{{ .Form.Code }} struct {
	{{- range .Form.Nodes }}
	{{ .Name }} {{ .Type }} // {{ .Description }}
	{{- end }}
}

func (f Form{{ .Form.Code }}) BuildPDF(r frl.Renderer) error {
	{{- range .Form.Nodes }}
	if err := {{ .Render }}; err != nil {
		return err
	}
	{{- end }}
	return nil
}
`
