// +build ignore

package main

import (
	"fmt"
	"html/template"
	"os"
	"path/filepath"
	"time"

	"github.com/haleyrc/forms/fdl"
	"github.com/haleyrc/forms/library"
)

func main() {
	tmpl := template.Must(template.New("").Parse(formTemplate))

	iter := library.Forms.Iter()
	for form := iter.Next(); form != nil; form = iter.Next() {
		fileName := fmt.Sprintf("form_%s.go", form.Code)
		path := filepath.Join("..", "form", fileName)

		f, err := os.Create(path)
		if err != nil {
			panic(err)
		}

		if err := tmpl.Execute(f, struct {
			Timestamp time.Time
			FileName  string
			Form      fdl.Form
		}{
			Timestamp: time.Now(),
			FileName:  fileName,
			Form:      *form,
		}); err != nil {
			panic(err)
		}

		f.Close()
	}
}

var formTemplate = `// Code generated by go generate; DO NOT EDIT.
//
// This file was generated at:
// {{ .Timestamp }}
//
// Data loaded from library.Forms.
//
// Name: {{ .FileName }}
package form

import (
	"fmt"
	
	"github.com/haleyrc/forms/fdl"
	"github.com/haleyrc/forms/frl"
)

{{ range .Form.Nodes }}
{{- .Types }}
{{- end -}}

type Form{{ .Form.Code }} struct {
	{{- range .Form.Nodes }}
	{{ .Name }} {{ .Type }}
	{{- end }}
}

func (f Form{{ .Form.Code }}) BuildPDF(r frl.Renderer) error {
	{{- range .Form.Nodes }}
	if err := {{ .Render }}; err != nil {
		return err
	}
	{{- end }}
	return nil
}
`
