// +build ignore

package main

import (
	"bytes"
	"fmt"
	"go/format"
	"io/ioutil"
	"os"
	"path/filepath"
	"text/template"
	"time"

	"github.com/haleyrc/forms/fdl"
	"github.com/haleyrc/forms/library"
)

func main() {
	tmpl := template.Must(template.New("").Parse(formTemplate))

	iter := library.Forms.Iter()
	for form := iter.Next(); form != nil; form = iter.Next() {
		if err := form.Valid(); err != nil {
			panic(err)
		}

		fileName := fmt.Sprintf("form_%s.go", form.Code)
		path := filepath.Join("..", "form", fileName)

		buff := bytes.Buffer{}
		if err := tmpl.Execute(&buff, struct {
			Timestamp time.Time
			FileName  string
			Form      fdl.Form
		}{
			Timestamp: time.Now(),
			FileName:  fileName,
			Form:      *form,
		}); err != nil {
			panic(err)
		}

		formatted, err := format.Source(buff.Bytes())
		if err != nil {
			panic(err)
		}

		if err := ioutil.WriteFile(path, formatted, os.ModePerm); err != nil {
			panic(err)
		}
	}
}

