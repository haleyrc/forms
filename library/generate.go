// +build ignore

package main

import (
	"bytes"
	"fmt"
	"go/format"
	"io/ioutil"
	"os"
	"path/filepath"
	"strings"
	"text/template"
	"time"

	"github.com/haleyrc/forms/fdl"
	"github.com/haleyrc/forms/library"
)

func main() {
	iter := library.Forms.Iter()
	for form := iter.Next(); form != nil; form = iter.Next() {
		if err := form.Valid(); err != nil {
			panic(err)
		}

		if err := generateGo(form); err != nil {
			panic(err)
		}

		if err := generatePython(form); err != nil {
			panic(err)
		}
	}
}

func generatePython(form *fdl.Form) error {
	tmpl := template.Must(template.New("").Funcs(template.FuncMap{
		"uppercase": strings.ToUpper,
	}).Parse(pythonFormTemplate))
	fileName := fmt.Sprintf("form_%s.py", form.Code)
	path := filepath.Join("..", "python", fileName)

	buff := bytes.Buffer{}
	if err := tmpl.Execute(&buff, struct {
		Timestamp time.Time
		FileName  string
		Form      fdl.Form
	}{
		Timestamp: time.Now(),
		FileName:  fileName,
		Form:      *form,
	}); err != nil {
		return err
	}

	if err := ioutil.WriteFile(path, buff.Bytes(), os.ModePerm); err != nil {
		return err
	}

	return nil
}

func generateGo(form *fdl.Form) error {
	tmpl := template.Must(template.New("").Parse(formTemplate))
	fileName := fmt.Sprintf("form_%s.go", form.Code)
	path := filepath.Join("..", "form", fileName)

	buff := bytes.Buffer{}
	if err := tmpl.Execute(&buff, struct {
		Timestamp time.Time
		FileName  string
		Form      fdl.Form
	}{
		Timestamp: time.Now(),
		FileName:  fileName,
		Form:      *form,
	}); err != nil {
		return err
	}

	formatted, err := format.Source(buff.Bytes())
	if err != nil {
		return err
	}

	if err := ioutil.WriteFile(path, formatted, os.ModePerm); err != nil {
		return err
	}

	return nil
}

var formTemplate = `// Code generated by go generate; DO NOT EDIT.
//
// This file was generated at:
//   {{ .Timestamp }}
//
// Data loaded from library.Forms.
//
package form

import (
	"fmt"

	"github.com/haleyrc/forms/fdl"
	"github.com/haleyrc/forms/frl"
)

{{ range .Form.Nodes }}
{{- .Types }}
{{- end -}}

// Form{{ .Form.Code }}
//
// {{ .Form.Description }}
type Form{{ .Form.Code }} struct {
	{{- range .Form.Nodes }}
	{{ .Name }} {{ .Type }} // {{ .Description }}
	{{- end }}
}

func (f Form{{ .Form.Code }}) BuildPDF(r frl.Renderer) error {
	{{- range .Form.Nodes }}
	if err := {{ .Render }}; err != nil {
		return err
	}
	{{- end }}
	return nil
}
`

var pythonFormTemplate = `'''
  Code generated by go generate; DO NOT EDIT.

  This file was generated at:
    {{ .Timestamp }}

  Data loaded from library.Forms.
'''

import frl
from enum import Enum

class {{ .Form.Code }}Option(Enum):
{{- range .Form.Nodes }}
  {{ .Name | uppercase }} = "{{ .Name }}"
{{- end }}

  @property
  def location(self):
    if self == StyleOption.SEDAN:
      return frl.Location(x=10, y=30)
    elif self == StyleOption.Truck:
      return frl.Location(x=30, y=30)
    else:
      raise Exception("invalid style option")

class Form{{ .Form.Code }}(frl.Form):

  name = "Form {{ .Form.Code }}"
  description = "{{ .Form.Description }}"

  def __init__(self, *, first_name=None, style=None):
    if first_name is None:
      raise frl.InvalidParameterException("first name must be provided")
    if style is None:
      raise frl.InvalidParameterException("style must be provided")

    self.first_name = first_name
    self.style = style

  def build_pdf(self, renderer):
    renderer.print_text(
      frl.Location(x=10, y=20),
      frl.FontSize(12),
      self.first_name)
    renderer.print_check(
      self.style.location,
      frl.Size(10))
`
